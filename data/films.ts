export function getEraStyle(year: number): { bg: string; text: string; tag: string; tagText: string } {
  if (year < 1930) return { bg: '#211608', text: '#f5e6d0', tag: '#4a3010', tagText: '#f5c878' }
  if (year < 1950) return { bg: '#200c14', text: '#f5d0e0', tag: '#45102a', tagText: '#f578b0' }
  if (year < 1970) return { bg: '#081420', text: '#d0e4f5', tag: '#102840', tagText: '#78c0f5' }
  if (year < 1990) return { bg: '#200808', text: '#f5d0d0', tag: '#451010', tagText: '#f57878' }
  if (year < 2010) return { bg: '#081a10', text: '#d0f0e0', tag: '#103520', tagText: '#78f0b0' }
  return { bg: '#100820', text: '#e0d0f5', tag: '#201040', tagText: '#c078f5' }
}
